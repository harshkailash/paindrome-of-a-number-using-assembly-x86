ASSUME CS:CODE, DS:DATA
DATA SEGMENT
STR DB "WUOOerUW$"
MSG DB "STRING IS A PALINDROME $"
MSG2 DB "STRING IS NOT A PALINDROME $"
DATA ENDS


CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
XOR AX,AX
LEA SI,STR
XOR CX,CX
LOOP1:
CMP STR[SI],'$'
JE PALINDROME
INC SI
INC CX
JMP LOOP1

PALINDROME:
LEA SI,STR
LEA DI,STR
DEC CX
ADD DI,CX
LOOP2:
MOV BL,STR[DI]
CMP STR[SI],BL
JNE NP
INC SI
DEC DI
LOOP LOOP2

LEA DX,MSG
MOV AH,09H
INT 21H
JMP EXIT

NP:
LEA DX,MSG2
MOV AH,09H
INT 21H

EXIT:
MOV AH,4CH
INT 21H


HLT
CODE ENDS
END START
